package com.example.college_student;

public class AddStudents {

    static int imageId;
    static  String fullname;
    static String age;
    static String address;
    static String gender;


    public static int getImageId() {
        return imageId;
    }

    public static void setImageId(int imageId) {
        AddStudents.imageId = imageId;
    }

    public static String getName() {
        return fullname;
    }

    public static void setName(String fullname) {
        AddStudents.fullname = fullname;
    }

    public static String getAge() {
        return age;
    }

    public static void setAge(String age) {
        AddStudents.age = age;
    }

    public static String getGender() {
        return gender;
    }

    public static void setGender(String gender) {
        AddStudents.gender = gender;
    }

    public static String getAddress() {
        return address;
    }

    public static void setAddress(String address) {
        AddStudents.address = address;
    }


}
